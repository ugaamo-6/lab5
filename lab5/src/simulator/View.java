
package simulator;

/**
 * The generall view of the simulation.
 * 
 * @author Johan Bråtendal
 * @author Gustav Mattsson
 * @author Jonas Jarnhäll Sjöman
 */
public class View {
	
	/**
	 * Prints beginning text, when simulation starts.
	 */
	public void beginInfoPrint(){	
		System.out.println("Started");
	}
	
	/**
	 * Prints end text, when simulation ends.
	 */
	public void endInfoPrint() {
		System.out.println("Closed.");	
	}
	

}


